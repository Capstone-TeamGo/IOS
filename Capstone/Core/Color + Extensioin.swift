import Foundation
import UIKit

extension UIColor {
    static let PrimaryColor : UIColor = {
        return UIColor(named: "PrimaryColor") ?? .white
    }()
    static let SecondaryColor : UIColor = {
        return UIColor(named: "SecondaryColor") ?? .white
    }()
    static let ThirdryColor : UIColor = {
        return UIColor(named: "ThirdryColor") ?? .white
    }()
    static let FourthryColor : UIColor = {
        return UIColor(named: "FourthryColor") ?? .white
    }()
    static let FifthryColor : UIColor = {
        return UIColor(named: "FifthryColor") ?? .white
    }()
    static let BackgroundColor : UIColor = {
        return UIColor(named: "BackgroundColor") ?? .white
    }()
}
